-- PARAMETER IN LOOP(START,CONDITION,INCREMENT/DECREMENT)

DELIMITER //
CREATE PROCEDURE WHILEDEMO()
BEGIN
DECLARE i INT DEFAULT 1;
DECLARE OUTPUT VARCHAR(20) DEFAULT '';
WHILE i<=5 DO -- SELECT i;
SET OUTPUT=CONCAT(OUTPUT,i,' ');
SET i=i+1;
END WHILE;
SELECT OUTPUT;
END //
