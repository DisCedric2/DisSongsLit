-- Q.1 CREATE A STORED PROCEDURE WITH NAME AS GET_EMPLOYEE THAT RETURN THE LIST OF EMPLOYEE WITH THEIR
-- FISRT_NAME, LAST_NAME, EMAIL, DEPARTMENT NAME OF ALL EMPLOYEES.

DELIMITER $
CREATE PROCEDURE GET_EMPLOYEE()
BEGIN
SELECT  E.FIRST_NAME, E.LAST_NAME, E.EMAIL, D.DEPARTMENT_NAME
FROM EMPLOYEES E INNER JOIN DEPARTMENTS D ON E.DEPARTMENT_ID=D.DEPARTMENT_ID;
END $

DROP PROCEDURE GET_EMPLOYEE;

CALL GET_EMPLOYEE();

