-- REPEAT OR UNTI LOOP
DELIMITER #
CREATE PROCEDURE REPEATLOOP()
BEGIN
DECLARE i INT;
DECLARE OUTPUT VARCHAR (20);
SET i=1;
SET OUTPUT='';
REPEAT SET OUTPUT=CONCAT(OUTPUT,I,' ');
SET i=i+1;
UNTIL i>5 END REPEAT;
SELECT OUTPUT;
END #
CALL REPEATLOOP;

-- LABEL LOOP
DELIMITER //
CREATE PROCEDURE LABELOOP()
BEGIN
DECLARE i INT DEFAULT 1;
DECLARE OUTPUT VARCHAR(20) DEFAULT '';
GO:LOOP
IF i>5 THEN LEAVE GO;
END IF;
SET OUTPUT=CONCAT(OUTPUT,i,' ');
SET i=i+1;
ITERATE GO;
END LOOP;
SELECT OUTPUT;
END //

CALL LABELOOP;
